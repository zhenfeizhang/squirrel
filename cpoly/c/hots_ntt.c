/// convert a polynomial into its NTT form
void hots_ntt(uint32_t *p) {
  unsigned int t, ht, i, j, j1, j2, l, m;
  uint32_t u, v, s;

  t = N;
  for (l = 0; l < 8; l++) {
    m = 1 << l;
    ht = t >> 1;
    i = 0;
    j1 = 0;
    while (i < m) {
      s = HOTS_NTT_TABLE[m + i];
      j2 = j1 + ht;
      j = j1;
      while (j < j2) {
        u = p[j];
        v = (uint32_t)(((uint64_t)p[j + ht]) * ((uint64_t)s) % 10571777);
        p[j] = ((u + v) % 10571777);
        p[j + ht] = (u + 10571777 - v) % 10571777;
        j++;
      }
      i++;
      j1 += t;
    }
    t = ht;
  }
}

/// convert an NTT form polynomial into its integer form
void hots_inv_ntt(uint32_t *p) {
  unsigned int t, m, hm, dt, i, j, j1, j2;
  uint32_t s, u, v;

  t = 1;
  m = N;

  while (m > 1) {
    hm = m >> 1;
    dt = t << 1;
    i = 0;
    j1 = 0;
    while (i < hm) {
      j2 = j1 + t;
      s = HOTS_INV_NTT_TABLE[hm + i];
      j = j1;
      while (j < j2) {
        u = p[j];
        v = p[j + t];
        p[j] = (u + v) % 10571777;
        p[j + t] = (uint32_t)(((uint64_t)(u + 10571777 - v)) * ((uint64_t)s) %
                              10571777);
        j++;
      }
      i++;
      j1 += dt;
    }
    t = dt;
    m = hm;
  }

  for (i = 0; i < N; i++) {
    p[i] = (uint32_t)(((uint64_t)p[i]) * 10530481 % 10571777);
  }
}
